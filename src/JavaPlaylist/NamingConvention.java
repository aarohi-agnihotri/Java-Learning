package firstPackage;

public class NamingConvention {
    public static void main(String[] args) {
        //case sensitive
        //can be letters, digits, dollar signs, or underscores
        //must begin with a letter, dollar sign ($), or underscore (_)
        //cannot use java keywords as variable names
        //camel case
        String myFirstProgramming = "java language";
        System.out.println(myFirstProgramming);
    }
}
